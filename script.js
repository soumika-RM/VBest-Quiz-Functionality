function submitAnswers() {
  const classSelect = document.getElementById("classSelect");
  const subjectSelect = document.getElementById("subjectSelect");
  const chapterSelect = document.getElementById("chapterSelect");
  const topicSelect = document.getElementById("topicSelect");

  const selectedClass = classSelect.value;
  const selectedSubject = subjectSelect.value;
  const selectedChapter = chapterSelect.value;
  const selectedTopic = topicSelect.value;

  if (selectedClass && selectedSubject && selectedChapter) {
    const topics = data[selectedClass][selectedSubject].chapters
      .find(chapter => chapter.name === selectedChapter).topics;

    const selectedQuestions = selectedTopic 
      ? topics.find(topic => topic.name === selectedTopic).questions
      : topics.flatMap(topic => topic.questions);

    const randomQuestions = selectedQuestions
      .sort(() => 0.5 - Math.random())
      .slice(0, 2);

    const questionsContainer = document.getElementById("questionsContainer");
    questionsContainer.innerHTML = '';

    randomQuestions.forEach((question, index) => {
      const questionDiv = document.createElement("div");
      questionDiv.innerHTML = `
        <p>${question.question}</p>
        ${question.options.map((option, i) => `
          <input type="radio" name="question${index}" value="${i}" id="question${index}option${i}">
          <label for="question${index}option${i}">${option}</label><br>
        `).join('')}
      `;
      questionsContainer.appendChild(questionDiv);
    });

    const resultBtn = document.createElement("button");
    resultBtn.textContent = "Calculate Result";
    resultBtn.onclick = calculateResult;
    questionsContainer.appendChild(resultBtn);
  }
}

function calculateResult() {
  const classSelect = document.getElementById("classSelect");
  const subjectSelect = document.getElementById("subjectSelect");
  const chapterSelect = document.getElementById("chapterSelect");
  const topicSelect = document.getElementById("topicSelect");

  const selectedClass = classSelect.value;
  const selectedSubject = subjectSelect.value;
  const selectedChapter = chapterSelect.value;
  const selectedTopic = topicSelect.value;

  const topics = data[selectedClass][selectedSubject].chapters
    .find(chapter => chapter.name === selectedChapter).topics;

  const selectedQuestions = selectedTopic
    ? topics.find(topic => topic.name === selectedTopic).questions
    : topics.flatMap(topic => topic.questions);

  let score = 0;

  selectedQuestions.forEach((question, index) => {
    const selectedOption = document.querySelector(`input[name="question${index}"]:checked`);
    if (selectedOption && parseInt(selectedOption.value) === question.correctAnswer) {
      score++;
    }
  });

  const resultContainer = document.getElementById("resultContainer");
  resultContainer.innerHTML = `Your result is ${score}/2`;
}